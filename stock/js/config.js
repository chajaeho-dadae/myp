/** 배열에서 랜덤 1개 선택 */
function pickRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

/** 배열에서 중복 없이 n개 선택 */
function pickRandomN(arr, n) {
  const shuffled = [...arr].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(n, arr.length));
}

/** 확률 테이블에서 티어 1개 뽑기 */
function rollTier() {
  const rand = Math.random();
  let cumul = 0;
  for (const tier of CHANGE_TIERS) {
    cumul += tier.prob;
    if (rand < cumul) return tier;
  }
  return CHANGE_TIERS[3]; // 유지 fallback
}

/** 티어에서 실제 변동률 계산 */
function rollChangeRate(tier) {
  return tier.min + Math.random() * (tier.max - tier.min);
}

/** 금액 한국식 포맷 (예: 1,234,567) */
function formatKRW(num) {
  return Math.round(num).toLocaleString('ko-KR') + '원';
}

/** 등락률 포맷 (예: +12.34%) */
function formatRate(rate) {
  const sign = rate >= 0 ? '+' : '';
  return sign + (rate * 100).toFixed(2) + '%';
}

/** 방 코드 생성 (영문 대문자 + 숫자 4자리) */
function generateRoomCode() {
  const chars = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
  return Array.from({ length: 4 }, () =>
    chars[Math.floor(Math.random() * chars.length)]
  ).join('');
}